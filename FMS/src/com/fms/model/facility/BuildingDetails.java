package com.fms.model.facility;

public class BuildingDetails {

	private double area;
	
	private int buildingID;
	private int facilityID;
	private int floors;
	private int numbOfElevators;
	private int numbOfEscalators;
	private int numbOfRooms;
	private int numbOfStaircases;
	
	
	public double getArea() {
		return area;
	}
	public void setArea(double area) {
		this.area = area;
	}
	public int getFacilityID() {
		return facilityID;
	}
	public void setFacilityID(int facilityID) {
		this.facilityID = facilityID;
	}
	public int getFloors() {
		return floors;
	}
	public void setFloors(int floors) {
		this.floors = floors;
	}
	public int getNumbOfElevators() {
		return numbOfElevators;
	}
	public void setNumbOfElevators(int numbOfElevators) {
		this.numbOfElevators = numbOfElevators;
	}
	public int getNumbOfEscalators() {
		return numbOfEscalators;
	}
	public void setNumbOfEscalators(int numbOfEscalators) {
		this.numbOfEscalators = numbOfEscalators;
	}
	public int getNumbOfRooms() {
		return numbOfRooms;
	}
	public void setNumbOfRooms(int numbOfRooms) {
		this.numbOfRooms = numbOfRooms;
	}
	public int getNumbOfStaircases() {
		return numbOfStaircases;
	}
	public void setNumbOfStaircases(int numbOfStaircases) {
		this.numbOfStaircases = numbOfStaircases;
	}
	public int getBuildingID() {
		return buildingID;
	}
	public void setBuildingID(int buildingID) {
		this.buildingID = buildingID;
	}
	
	

}
